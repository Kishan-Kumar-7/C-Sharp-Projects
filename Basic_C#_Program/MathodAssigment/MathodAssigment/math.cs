using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathodAssigment
{
    internal class math
    {
        public static int Addition(int val1, int val2)
        {
            return val1 + val2;
        }
        public static int multi(int val1, int val2)
        {
            return val1 * val2;
        }
        public static int divi(int val1, int val2)
        {
            return val1 / val2;
        }
        public static int mod(int val1, int val2)
        {
            return val1 % val2;
        }
        public static int perc(int val1, int val2)
        {
            return val1 * val2 / 100;
        }
    }
}
